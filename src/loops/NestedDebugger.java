package loops;

public class NestedDebugger {
    public static void main(String[] args) {
//this it to understand the nested loops
        for(int i = 0 ; i < 100 ; i++){
            for(int j = 0 ; j <= 10 ; j+=2){
                System.out.println(i + "  " + j);
            }
        }
        }
    }

